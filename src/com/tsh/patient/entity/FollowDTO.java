package com.tsh.patient.entity;

import com.tsh.common.BaseDTO;

public class FollowDTO extends BaseDTO
{
	private Long id;
	private Long userId;
	private String followDate;
	private String followedDate;
	private String status;
	private String result;

	public Long getId()
	{
		return id;
	}

	public void setId(Long id)
	{
		this.id = id;
	}

	public Long getUserId()
	{
		return userId;
	}

	public void setUserId(Long userId)
	{
		this.userId = userId;
	}

	public String getFollowDate()
	{
		return followDate;
	}

	public void setFollowDate(String followDate)
	{
		this.followDate = followDate;
	}

	public String getFollowedDate()
	{
		return followedDate;
	}

	public void setFollowedDate(String followedDate)
	{
		this.followedDate = followedDate;
	}

	public String getStatus()
	{
		return status;
	}

	public void setStatus(String status)
	{
		this.status = status;
	}

	public String getResult()
	{
		return result;
	}

	public void setResult(String result)
	{
		this.result = result;
	}
	

}
